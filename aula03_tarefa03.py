a=float(input("Digite o 1º número : "))
b=float(input("Digite o 2º número : "))

soma=a+b
sub = a-b
mult = a*b
div = a/b
divi = a//b
modulo = a%b
exp = a**b

print(f"O resultado da soma é {soma}")
print(f"O resultado da subtração é {sub}")
print(f"O resultado da multiplicação é {mult}")
print(f"O resultado da divisão é {div:.2f}")
print(f"O resultado da divisão real é {divi}")
print(f"O resultado do módulo é {modulo}")
print(f"O resultado da exponenciação é {exp}")